package com.example.projectmonitoringapp.model;

public class RcUser {
    private String message;
    private String onLive;
    private String phone;
    private String position;
    private String registerDate;
    private String userId;
    private String username;
    private String unsealDate;

    public String getUnsealDate() {
        return unsealDate;
    }

    public void setUnsealDate(String unsealDate) {
        this.unsealDate = unsealDate;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getOnLive() {
        return onLive;
    }

    public void setOnLive(String onLive) {
        this.onLive = onLive;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getRegisterDate() {
        return registerDate;
    }

    public void setRegisterDate(String registerDate) {
        this.registerDate = registerDate;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
